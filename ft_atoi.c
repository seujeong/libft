#include "libft.h"

int	ft_atoi(const char *str)
{
	long	i;
	long	signcheck;
	int		num;

	i = 0;
	signcheck = 1;
	num = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		signcheck *= -1;
		i++;
	}
	if (str[i] == '+' && signcheck != -1)
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	return (signcheck * num);
}
